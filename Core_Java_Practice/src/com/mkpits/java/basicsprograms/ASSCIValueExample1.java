public class ASSCIValueExample1
{
    public static void main(String[] args)
    {

        char ch1 = 'x';
        char ch2 = 'y';

        int asciivalue1 = ch1;
        int asciivalue2 = ch2;
        System.out.println("The ASCII value of " + ch1 + " is: " + asciivalue1);
        System.out.println("The ASCII value of " + ch2 + " is: " + asciivalue2);
    }
}

